/**
 *
 *  * _isupper - function to check for uppercase in a character argument
 *
 *   * @c: Argument for the function
 *
 *    * Return: Always 1 for true and 0 for false
 *
 *     */

int  _isupper(int c)

{

		if (isupper(c))

				{

							return (1);

								}

			else

					{

								return (0);

									}



				return (0);



}
